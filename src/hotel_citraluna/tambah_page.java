    private void txtKamarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtKamarActionPerformed
        // TODO add your handling code here:

        // Total pembayaran berdasarkan item yang dipilih dari ComboBox
        String selectedKamar = (String) txtKamar.getSelectedItem();
        int hargaKamar = 0;
        String kodeKamar = "";
        switch (selectedKamar) {
            case "Kamar Standar":
                hargaKamar = 600000;
                kodeKamar = "SC";
                break;
            case "Kamar Deluxe":
                hargaKamar = 1200000;
                kodeKamar = "DC";
                break;
            case "Suite Eksekutif":
                hargaKamar = 2000000;
                kodeKamar = "EXC";
                break;
            case "Karaoke + Pemandu":
                hargaKamar = 450000;
                kodeKamar = "KP";
                break;
            // Tambahkan case untuk kamar lainnya sesuai kebutuhan

            default:
                break;
        }

        int nomorKamarRandom = (int) (Math.random() * 90) + 10;
        ccNomorKamar.setText(String.valueOf(kodeKamar + nomorKamarRandom));

        // Hitung total pembayaran
        ccTotalPembayaran.setText(String.valueOf(hargaKamar));
    }//GEN-LAST:event_txtKamarActionPerformed

