    private void tambahTamu(String nama_tamu, String alamat_tamu) {
        try (Connection dbconn = Konfig.configDB()) {
            if (dbconn != null) {
                String query = "INSERT INTO tamu (nama_tamu, alamat_tamu) VALUES (?, ?)";
                try (PreparedStatement st = dbconn.prepareStatement(query)) {
                    st.setString(1, nama_tamu);
                    st.setString(2, alamat_tamu);

                    int res = st.executeUpdate();

                    JOptionPane.showMessageDialog(this, "User Data Inserted.", "Successful", JOptionPane.INFORMATION_MESSAGE);

                    // Membersihkan isian kolom setelah operasi pemasukan data
                    txtNamaTamu.setText("");
                    txtAlamatTamu.setText("");

                } catch (SQLException ex) {

                    JOptionPane.showMessageDialog(this, "Failed to Insert User Data.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                System.out.println("The Connection Not Available.");
            }
        } catch (SQLException ex) {

        }
    }

